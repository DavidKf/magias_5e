  final String name;
  final String duration;
  final bool concentration;
  final String school;
  final bool ritual;
  final String classes;
  final String conjurationTime;
  final String reach;
  final String components;
  final String description;
  final String superiorLevel;

  Spell(
    this.level,
    this.name,
    this.duration,
    this.concentration,
    this.school,
    this.ritual,
    this.classes,
    this.conjurationTime,
    this.reach,
    this.components,
    this.description,
    this.superiorLevel,
  );
}
